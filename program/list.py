# The difference between list and array: Array contains same datatypes , list contains different datatypes
# Inside the list you can put any types of number

friends = ["Kevin", "Karen", "Jim", "oscars", "harsh"]
print(friends)
print(friends[0])
print(friends[1])
print(friends[2])

# From the backward sides
print(friends[-1])

# It starts from the index 1 and goes till the end
print(friends[1:])

# It will start from 1 and finishes at 2
print(friends[1:3])

# update a list
friends[1] = "Mike"
print(friends)

friends.append("Uditi")
friends.insert(0, "Sahil")
